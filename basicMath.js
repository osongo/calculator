//create function that mimic basicMath operations
//addition,multiplication,division and subtraction

//addition
function add (a,b) {
    return a + b;
}

//multiplication
function multiply (a,b) {
    return a * b;
}

//division
function divide (a,b) {
    return a / b;
}

//subtraction
function subtract (a,b) {
    return a - b;
}